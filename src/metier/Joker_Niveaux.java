package metier;

public class Joker_Niveaux {
	private int joker;
	private int niveau;
	public Joker_Niveaux(int joker, int niveau) {
		super();
		this.joker = joker;
		this.niveau = niveau;
	}
	public Joker_Niveaux() {
		super();
	}
	
	public int getJoker() {
		return joker;
	}
	public void setJoker(int joker) {
		this.joker = joker;
	}
	public int getNiveau() {
		return niveau;
	}
	public void setNiveau(int niveau) {
		this.niveau = niveau;
	}
	
	

}
